package com.viola.eatfit;

public class ShoppingItem {
    private int id;
    private String name;

    public ShoppingItem(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(int id) {
        this.id = id;
    }
}
